package com.deucate.kartik.billmk.Spent;


public class SpentGs {

    private int Category;
    private String Date;
    private String Reason;
    private int Rs;
    private String Time;

    public SpentGs() {
    }

    public int getCategory() {
        return Category;
    }

    public String getDate() {
        return Date;
    }

    public String getReason() {
        return Reason;
    }

    public int getRs() {
        return Rs;
    }

    public String getTime() {
        return Time;
    }
}
